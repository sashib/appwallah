package com.appwallah.ideawallah.models;


public class Idea {

    public String idea;
    public String uid;
    public String date;
    public boolean global;

    public String getUid() {
        return uid;
    }

    public String getIdea() {
        return idea;
    }

    public String getDate() {
        return date;
    }

    public boolean isGlobal() {
        return global;
    }


}