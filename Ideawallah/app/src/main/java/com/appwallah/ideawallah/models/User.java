package com.appwallah.ideawallah.models;


public class User {


    public String name;
    public String email;
    public String date;
    public String userId;

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getDate() {
        return date;
    }

    public String getUserId() {
        return userId;
    }


}